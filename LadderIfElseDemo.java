public class LadderIfElseDemo 
{
	public static void main(String args[])
	{
		int i = 4;	//Consider i is the choice
		if(i==1)
		{
			System.out.println("Choice is One");
		}
		else if(i==2)
		{
			System.out.println("Choice is Two");
		}
		else if(i==3)
		{
			System.out.println("Choice is Three");
		}
		else if(i==4)
		{
			System.out.println("Choice is Four");
		}
		else 	// this is a optional statement
		{
			System.out.println("Choice is Invalid");
		}
	}
}
