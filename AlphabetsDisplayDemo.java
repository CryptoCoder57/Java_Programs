public class AlphabetsDisplayDemo
{
	public static void main(String args[])
	{
		System.out.println("Alphabets in lower case are : ");
		for(char c='a';c<='z';c++)
		{
			System.out.print(c+" ");
		}
		System.out.println();
		System.out.println("======================");
		System.out.println("Alphabets in upper case are : ");
		for(char c='A';c<='Z';c++)
		{
			System.out.print(c+" ");
		}
	}
}
